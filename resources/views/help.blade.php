				<h3 id="tips">Tips for Conversing with the bot</h3>
				<div class="helpcontent4">

					<p>
						To start off, when the bot says hi to you. To have an effective conversation just greet it back with a simple hello or hey. Then you can see the tips below to know how you are going to converse with it. I hope you did not come here though expecting a list of questions to ask the bot.
					</p>
					<p>
						I will give you only one free direct question. <em>You can ask the bot its age by asking it the question, "how old are you?" of course without the quotes.</em>
					</p>
					<p>
						Other than giving you direct questions to ask the bot, I will give you some tips. And then you can go and find out if you can have a conversation with it. I promise you the tips are very helpful, it is better that trying to hold a conversation without knowing anything.
					</p>
					<p>
						<strong>Off we start</strong> <br>
						It is all about having a conversation with the bot, just like a real human conversation would be carried out. Check for keywords when the bot replies. <br> <br>

						Take for an example, when you ask the bot about the development team, and it replies "I was developed by <em>Team Asclepius</em> development team"
						From the reply, you can continue the conversation with the main keyword Team Asclepius. Now you can ask it <em>what this Team Asclepuius is?</em> It's next reply will probably have another keyword. Therefore check for keywords and on and on you will go with the conversation.
					</p>
					<h4>What if I find no other keyword is working</h4>
					<p>
						well if no other keyword is working for you, worry not. You can find simple short questions
						that you can ask the bot. It will be like holding <em>small talk</em> with the bot. <br>
						you can ask it one of the following.
					</p>
					<p>"how are you?" --> show genuine concern for the bot</p>
					<p>"what time is it?" -->ask the bot the time</p>
					<p>"what is my age?" -->ask the bot your age</p>
					<p>"what is your name?" --> ask the bot it's name</p>
					<p>Ofcourse all this questions you type them in without the quotes</p>
